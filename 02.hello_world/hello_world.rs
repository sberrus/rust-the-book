///Estos son alertas de varias lineas y soportan MARKDOWN

/// La función `main` es una función especial ya que es el punto de entrada
/// de todas las aplicaciones desarrolladas en rust.
///
/// Dentro de los parentesis es donde según el caso recibiriamos los parámetros.
fn main(){
    /// las ejecuciones de funciones que finalizan con `!` son porque estas
    /// son macros. Los macros son funciones especiales que permiten mofificar
    /// el codigo fuente en tiempo de compilación. 
    ///
    /// Son como funciones pero con capacidades de modificación del código fuente.
    println!("Hello, world!"); // Esto es un comentario de linea
}


